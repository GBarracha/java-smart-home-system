
/**
 * A interface atuador permite definir o método act que será implementado em todas
 * as classes que implementam esta mesma interface
 * 
 * @author Gonçalo Barracha, Rodrigo Cardoso
 * @version 17/05/2024
 */

public interface Atuador
{
    /**
     * Método geral que será implementado nas classes descendentes com as suas
     * funcionalidades especificas.
     * Este método deverá atuar/realizar algum comportamento definido por cada classe
     */
    void act();
}
